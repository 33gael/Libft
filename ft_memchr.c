#include "libft.h"

void	*memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (str[i] != c && i < n)
		i++;
	if (str[i] == c)
		return ((void *)s + i);
	else
		return (NULL);
}
